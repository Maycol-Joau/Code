
package algebraica;


public class Algebraica {

    public static void main(String[] args) {

// a) p+2rs
    double p=1,r=1 ,s=1;
    double resultadoA = p + 2 * r * s;

// b) -x+3br-2k
    double x=1,b=1,k=1;
    double resultadoB = -x + 3 * b * r - 2 * k;

// c) 2(x+y+z)
    double y=1,z=1;
    double resultadoC = 2 * (x + y + z);

// d) 3abc-d+3kf-2gi
    double a=1,c=1,d=1,f=1,g=1,i=1;
    double resultadoD = 3 * a * b * c - d + 3 * k * f - 2 * g * i;

// e) a+4b+3d-2(3f+eg-h)
    double e=1,h=1;
    double resultadoE = a + 4 * b + 3 * d - 2 * (3 * f + e * g - h);

// f) 3rst-2(p+2q)
    double t=1,q=1;
    double resultadoF = 3 * r * s * t - 2 * (p + 2 * q);

// g) a elevado al 2 + b elevado al 2 - 2e
    double resultadoG = Math.pow(a, 2) + Math.pow(b, 2) - 2 * e;

// h) x / y * (z + w)
    double w=1;
    double resultadoH = (x / y) * (z + w);

// i) m / n + p + 5y
    double m=1,n=1;
    double resultadoI = (m / n) + p + (5 * y);
    
// j) (m + a) / (p - q)
    double resultadoJ = (m + a) / (p - q);
    
// k) Math.pow(p, 2 * y + 3) + Math.pow(2, x - 1)
    double resultadoK = Math.pow(p, 2 * y + 3) + Math.pow(2, x - 1);

    }
}



   
    

