    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

    try {
        int idClave = Integer.parseInt(txtClave.getText());
        String nombre = txtNombre.getText();
        String categoria = txtCategoria.getText();
        double precio = Double.parseDouble(txtPrecio.getText());
        int cantidad = Integer.parseInt(txtCantidad.getText());

        conexion con = new conexion();
        int respuesta = con.Rproducto(idClave, nombre, categoria, precio, cantidad);
        if (respuesta == 1) {
            JOptionPane.showMessageDialog(null, "El registro fue exitoso");
        } else {
            JOptionPane.showMessageDialog(null, "Hubo un error en el registro");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Por favor, ingresa valores válidos.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Hubo un error: " + e.getMessage());
    }
    

    }//GEN-LAST:event_btnAgregarActionPerformed

