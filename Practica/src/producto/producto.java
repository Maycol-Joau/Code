    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int idClave=Integer.parseInt(txtClave.getText());
        conexion con=new conexion();
        SettGetter encontrar= con.buscar(idClave);
        if(encontrar!=null){
            txtClave.setText(String.valueOf(encontrar.getIdClave()));
            txtNombre.setText(String.valueOf(encontrar.getNombre()));
            txtCategoria.setText(String.valueOf(encontrar.getCategoria()));
            txtPrecio.setText(String.valueOf(encontrar.getPrecio()));
            txtCantidad.setText(String.valueOf(encontrar.getCantidad()));
            JOptionPane.showMessageDialog(null, "Producto encontrado");

        }else{
            JOptionPane.showMessageDialog(null, "Producto no encontrado");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        try {
            int idClave=Integer.parseInt(txtClave.getText());
            conexion con = new conexion();
            boolean eliminar= con.eliminar(idClave);
            if(eliminar){
                txtClave.setText("");
                txtNombre.setText("");
                txtCategoria.setText("");
                txtPrecio.setText("");
                txtCantidad.setText("");
                JOptionPane.showMessageDialog(null, "Producto eliminado correctamente");
            }else{
                JOptionPane.showMessageDialog(null, "Hubo un error");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Porfavor, introduce un ID valido ");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Hubo un error" +e.getMessage());
        }

    }//GEN-LAST:event_jButton4ActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
    
        try {
        int idClave = Integer.parseInt(txtClave.getText());
        String nombre = txtNombre.getText();
        String categoria = txtCategoria.getText();
        double precio = Double.parseDouble(txtPrecio.getText());
        int cantidad = Integer.parseInt(txtCantidad.getText());
        conexion con = new conexion();
        boolean modificar=con.modificar(idClave, nombre, categoria, precio, cantidad);
        if(modificar){
            JOptionPane.showMessageDialog(null, "EL producto se modifico correctamente");
        }else{
            JOptionPane.showMessageDialog(null, "EL producto no se pudo modificar correctamente");
        }
        } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Porfavor, ingrese valores validos");

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Hubo un error"+e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

